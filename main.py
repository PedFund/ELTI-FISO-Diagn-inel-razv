def calc_lab(time, errors, reached, limit):
    """–†–∞—Å—á—ë—Ç –±–∞–ª–ª–æ–≤ –¥–ª—è –ª–∞–±–∏—Ä–∏–Ω—Ç–æ–≤"""
    try:
        time = float(time)
    except:
        return 0
    try:
        errors = int(errors)
    except:
        errors = 0
    if isinstance(reached, str) and reached.strip() == "–ù–µ—Ç":
        return 0
    if time > limit:
        return 0
    if errors == 0:
        return 3
    if errors == 1:
        return 2
    if 2 <= errors <= 5:
        return 1
    return 0


def attention_index(rings, errors):
    """–ò–Ω–¥–µ–∫—Å –≤–Ω–∏–º–∞–Ω–∏—è –ø–æ –∫–æ–ª—å—Ü–∞–º"""
    try:
        rings = float(rings)
    except:
        rings = 0
    try:
        errors = float(errors)
    except:
        errors = 0
    return 0.5 * rings - (2.8 * errors) / 60


def categorize(value):
    """–ö–∞—Ç–µ–≥–æ—Ä–∏–∑–∞—Ü–∏—è —É—Ä–æ–≤–Ω—è —Ä–∞–∑–≤–∏—Ç–∏—è"""
    if value < 0.33:
        return "–Ω–∏–∂–µ –Ω–æ—Ä–º–∞—Ç–∏–≤–Ω–æ–≥–æ"
    elif value <= 0.66:
        return "–Ω–æ—Ä–º–∞—Ç–∏–≤–Ω—ã–π"
    else:
        return "–≤—ã—à–µ –Ω–æ—Ä–º–∞—Ç–∏–≤–Ω–æ–≥–æ"


def extract_town(org_name):
    """–ò–∑–≤–ª–µ—á–µ–Ω–∏–µ –Ω–∞—Å–µ–ª—ë–Ω–Ω–æ–≥–æ –ø—É–Ω–∫—Ç–∞"""
    match = re.search(r"\((.*?)\)", str(org_name))
    if match:
        return match.group(1).split(";")[0].strip()
    return None


def sort_key_town(name):
    """–ö–ª—é—á —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏ –Ω–∞—Å–µ–ª—ë–Ω–Ω—ã—Ö –ø—É–Ω–∫—Ç–æ–≤"""
    if pd.isna(name):
        return (999, "")
    name = str(name).strip()
    if name == "–≥.–ú–æ—Å–∫–≤–∞":
        return (0, name)
    elif name.startswith("–≥."):
        return (1, name)
    elif name.startswith(("—Ä.–ø.", "–ø.", "–ø–æ—Å.")):
        return (2, name)
    elif name.startswith("—Å."):
        return (3, name)
    elif name.startswith("–¥."):
        return (4, name)
    elif name.startswith("—Å—Ç."):
        return (5, name)
    return (6, name)


